/*
 * A simple self-replicating program, because why not
 *
 * - compile
 * - copy executable to somewhere else
 * - run
 * - program will write itself
 */

#include <stdio.h>
#include <string.h>

int main()
{
	int i;
	FILE *f;

	f = fopen("live.c", "w");

	fputs("char *program = \"", f);
	for (i = 0; i < strlen(program); i++)
		fprintf(f, "\\x%x", (unsigned int)program[i]);
	fputs("\";\n\n", f);

	fputs(program, f);

	fclose(f);
	return 0;
}
